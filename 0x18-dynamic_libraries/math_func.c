#include <stdio.h>

/**
*add - This add two integers
*@a: First integer
*@b: Second integer
*Return: Addition
*/

int add(int a, int b)
{
	return (a + b);
}

/**
*div - This divide two integers
*@a: First integer
*@b: Second integer
*Return: final result
*/

int div(int a, int b)
{
	return (a / b);
}

/**
*mul - This multiply two integers
*@a: First integer
*@b: Second integer
*Return: Result
*/

int mul(int a, int b)
{
	return (a * b);
}

/**
*sub - This subtract two integers
*@a: First integer
*@b: Second integer
*Return: Subtractwd result
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
*mod - mod of two integers
*@a: First integer
*@b: Second integer
*Return: final result
*/

int mod(int a, int b)
{
	return (a % b);
}
